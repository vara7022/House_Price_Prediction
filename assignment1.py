print(df.tail())

# Dataset shape
print("Shape:", df.shape)

# Data types
print(df.dtypes)

# Missing values
print(df.isnull().sum())

import matplotlib.pyplot as plt

# Scatter Plot
plt.scatter(df["Size_sqft"], df["Price"])
plt.title("House Size vs Price")
plt.xlabel("Size (sqft)")
plt.ylabel("Price")
plt.show()

